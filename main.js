let frame3 = document.querySelector(".frame3");
let frame3_lowText = document.querySelector(".frame3_text");
let frame3_icon = document.querySelector(".frame3_icon");
let muddySpace1 = document.querySelector(".muddy_space1");
let titleText1 = document.querySelector(".title_text1");
let insideText1 = document.querySelector(".inside_text1");
frame3.addEventListener("mouseover", () => {
    frame3_lowText.style.display = "none"
    frame3_icon.style.display = "none"
    muddySpace1.style.display="block"
    titleText1.style.animation = "slider_title_text 0.8s ease-out";
    insideText1.style.animation = "slider_inside_text 1s ease-in-out";
});
frame3.addEventListener("mouseout", () => {
    frame3_lowText.style.display = ""
    frame3_icon.style.display = ""
    muddySpace1.style.display=""
});



